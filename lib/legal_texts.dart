const String terminosCliente = '''
1. Objeto: MaterialesYa es una plataforma tecnológica que conecta a compradores con comercios de materiales de construcción y repartidores independientes. MaterialesYa no vende ni fabrica los productos ofrecidos: actúa como intermediario tecnológico entre comercios, repartidores y clientes.

2. Registro: el usuario declara que los datos provistos son verdaderos y se compromete a mantenerlos actualizados.

3. Pedidos: al confirmar un pedido, el cliente acepta el precio mostrado (productos + envío) al momento de la compra. Los precios son establecidos por cada comercio de forma independiente.

4. Pagos: los pagos se procesan a través de Mercado Pago. MaterialesYa no almacena datos de tarjetas de crédito/débito.

5. Cancelaciones: el cliente puede cancelar sin costo antes de que el comercio acepte el pedido o mientras esté en preparación. Una vez que el repartidor retiró el pedido, no es posible cancelar.

6. Responsabilidad sobre los productos: la calidad, descripción y stock de los productos es responsabilidad exclusiva del comercio que los publica. MaterialesYa facilita el canal de reclamo pero no garantiza ni responde por defectos de fabricación de los productos.

7. Reclamos: el cliente puede iniciar un reclamo dentro de las 24 horas posteriores a la entrega a través de la app.

8. Conducta: el cliente se compromete a brindar una dirección de entrega correcta y estar disponible para recibir el pedido en el horario estimado.

9. Modificaciones: MaterialesYa podrá modificar estos términos, notificando a los usuarios a través de la app.
''';

const String politicaPrivacidad = '''
